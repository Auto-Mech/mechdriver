def read_lj_dct(bath, target):
    """ Read the LJ_DCT
    """

    bath_dct = LJ_DCT.get(bath, None)
    if bath_dct is not None:
        lj_params = bath_dct.get(target, None)
    else:
        lj_params = None

    return lj_params


